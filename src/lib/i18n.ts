const reverseUiLiteralTranslations = Object.fromEntries(
  Object.entries(uiLiteralTranslations).map(([english, chinese]) => [chinese, english])
);

export function translateLiteral(text: string, language: 'en' | 'zh') {
  const normalized = text.replace(/\s+/g, ' ').trim();
  if (!normalized) return text;

  const dictionary = language === 'zh' ? uiLiteralTranslations : reverseUiLiteralTranslations;
  const translated = dictionary[normalized];
  if (!translated) return text;

  const leading = text.match(/^\s*/)?.[0] ?? '';
  const trailing = text.match(/\s*$/)?.[0] ?? '';
  return `${leading}${translated}${trailing}`;
}

export function useTranslation(language: 'en' | 'zh') {
  return useCallback(function t(key: string): string {
    const keys = key.split('.');
    let value: TranslationValue | TranslationMap | undefined = translations[language];
    for (const k of keys) {
      if (value && typeof value === 'object') {
        value = value[k];
      } else {
        return key;
      }
    }
    return typeof value === 'string' ? value : key;
  }, [language]);
}
